	<h2 class="title">内核跑分评测方法</h2>
	
	1.	javascript性能支持：
通过Google官方的Javascript脚本测试集，全面检测浏览器的Javascript性能。此测试分数越高越好。
测试地址：http://octane-benchmark.googlecode.com/svn/latest/index.html
<br>
2.	HTML5支持：
测试项包括：解析规则，画布，视频，音频，元素，表单，用户交互，历史和导航，微数据，网页应用，安全，多样性，位置和方向，WebGL，通信，文件，存储，工作人员，本地多媒体，通知等。此测试分数越高越好。
测试地址：http://chrome.360.cn/test/html5/index.html
<br>

3.	CSS3支持：
测试项包括：JavaScript、CSS3动画、CSS3变形、CSS3文字阴影、自定义WEB开放式字体、Unicode编码等等。数值越小支持越好。
测试地址：http://ie.microsoft.com/testdrive/performance/robohornetpro/
<br>

4.	Acid3：
Acid3是一份网页浏览器及设计软件之标准相容性的测试网页，Acid3是目前Web标准基准测试中最严格的一个,对DOM和JavaScript有着严格的约束,其测试焦点集中在ECMAScript、DOM Level 3、Media Queries和data: URL。浏览器开启此测试网页后，页面会不断加载功能、直接给予分数，满分为100分。
测试地址：http://acid3.acidtests.org/

	
