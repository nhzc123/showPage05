	<h2 class="title">游戏评测方法</h2>
	
	
	
	<!--表格-->
	<h4 style="font-size:22px;">名词解释：</h4>
		<li>FPS: Frame Per Second，游戏过程中的每秒帧数；该指标能够影响用户的游戏体验，FPS值越高表示游戏画面越流畅、体验越好；
	</li><li>游戏扫描率：指游戏加速器在用户电脑上能扫出来的游戏数目，占用户电脑上安装的总游戏数目的比例；该比例越高，表示游戏加速器越完善，能够对更多的用户游戏自动进行加速支持；</li>

	<br>
	<br>
	<h4 style="font-size:22px;">评测方法：</h4>
	
    <li>	在测试机器上进行游戏，在游戏过程中，分别记录使用PCF和竞品在优化前、后的FPS值并统计平均值；然后对比PCF和竞品对FPS的提升效果；记录FPS值的工具是Fraps.exe，一个用来记录FPS的第三方软件。</li>
	<li>在测试机上安装流行网站上的top 20游戏，然后分别使用PCF和竞品对测试机进行扫描，计算PCF和竞品对已安装游戏的扫描率。</li>
	
	

	
	
