	<!--表格-->
	<h4 style="font-size:22px;">名词解释：</h4>
		<li>系统垃圾：指用户电脑上，windows系统自带的一些软件产生的诸如temp、bak、log类的文件，这类文件占用了用户的磁盘空间且可以被删除，同时它们是操作系统执行任务式产生的，所以称之为“系统垃圾”；
	</li><li>第三方软件垃圾：指用户电脑上自行安装的软件，在运行过程中产生的一些占用磁盘空间、且可以被删除的文件，它们广泛存在与软件的安装目录、临时存储目录下。</li>

	
	<h4 style="font-size:22px;">评测方法：</h4>
	
    <li>系统垃圾清理上：对比PCF和竞品在公共清理点上的深度；不同清理点，则对比广度；</li>
	<li>第三方软件清理上：对比支持的软件款数，某些软件的清理规则；</li>

	
	
	
	
	
	

	
	
