	<h2 class="title">响应时间评测方法</h2>
	
	用两台测试机搭建测试环境，一台为server，另一台为client。server根据client发送的指令进行打开关闭浏览器、新建Tab页和切换Tab页等操作；client除了向server发送操作指令外，还对server屏幕进行截图，通过计算截图相似度来判断一条指令是否执行完毕并计算出执行指令时间即浏览器的响应时间。
	
