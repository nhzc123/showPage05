	<!--表格-->
	<h4 style="font-size:22px;">指标说明：</h4>
		<li>	安装PCF后增加的时间：安装PCF前用户开机时间为T0，安装PCF后，用户开机时间为T1；则安装PCF后增加的时间为T1-T0；该指标体现了安装PCF对用户电脑开机速度的影响；
	</li><li>PCF优化后减少的时间：安装PCF后用户开机时间为T1，在此基础上使用PCF进行一键优化之后，用户的开机时间变为T2；则PCF优化后减少的时间为T1-T2；该指标体现了PCF对用户电脑开机时间的优化能力；</li>
	
	<li>“安装Soluto后增加的时间”和“Soluto优化后减少的时间”同上；</li>

	<br>
	<br>
	
	<h4 style="font-size:22px;">评测方法：</h4>
	
    <li>	在五台实体测试机上进行测试，分别测试初始开机时间、安装PCFaster后的开机时间、使用PCFaster优化后的开机时间、安装竞品后的开机时间、使用竞品优化后的开机时间；然后利用这些数据计算PCFaster和竞品在安装后分别会带来多少开机时间的增长、优化后分别能带来多少开机时间的降低；</li>
	<li>上述五台测试机上的环境分别是：win7-32两台、winXP-32、winXP-64、win7-64；五台机器分别使用了泰国电脑市场上流行的五张安装盘进行安装；</li>
	<li>测试开机时间的工具分别是Boost Racer、Windows Kits；Windows Kits是微软的工具包，用来测试开机时间会比较可靠，但是只支持win7以上的操作系统；所以使用了Boost Racer工具来对winXP的系统开机时间进行测试，该工具没有Windows Kits稳定可靠。</li>
	
	
	

	
	
